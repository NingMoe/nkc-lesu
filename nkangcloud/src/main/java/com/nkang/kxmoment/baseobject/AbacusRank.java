package com.nkang.kxmoment.baseobject;

public class AbacusRank {
	public String openID;
	public String type;
	public String numCount;
	public String lengthMax;
	public String lengthMin;
	public String speed;
	public String rightRate;
	public String way;
	public String getOpenID() {
		return openID;
	}
	public void setOpenID(String openID) {
		this.openID = openID;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getNumCount() {
		return numCount;
	}
	public void setNumCount(String numCount) {
		this.numCount = numCount;
	}
	public String getLengthMax() {
		return lengthMax;
	}
	public void setLengthMax(String lengthMax) {
		this.lengthMax = lengthMax;
	}
	public String getLengthMin() {
		return lengthMin;
	}
	public void setLengthMin(String lengthMin) {
		this.lengthMin = lengthMin;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getRightRate() {
		return rightRate;
	}
	public void setRightRate(String rightRate) {
		this.rightRate = rightRate;
	}
	public String getWay() {
		return way;
	}
	public void setWay(String way) {
		this.way = way;
	}
	
}
